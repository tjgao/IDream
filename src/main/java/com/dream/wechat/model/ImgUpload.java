package com.dream.wechat.model;

public class ImgUpload {
	private String uploadImgName;
	private String uploadImgDesc;
	private int aId;
	public String getUploadImgName() {
		return uploadImgName;
	}
	public void setUploadImgName(String uploadImgName) {
		this.uploadImgName = uploadImgName;
	}
	public String getUploadImgDesc() {
		return uploadImgDesc;
	}
	public void setUploadImgDesc(String uploadImgDesc) {
		this.uploadImgDesc = uploadImgDesc;
	}
	public int getaId() {
		return aId;
	}
	public void setaId(int aId) {
		this.aId = aId;
	}
}
